public class Goal {

    public int day;
    public int month;
    public int year;
    public String name;
    public double bonus;
    public Goal(int day, int month, int year, String name, double bonus){
        this.day = day;
        this.month = month;
        this.year = year;
        this.name = name;
        this.bonus = bonus;
    }

}

